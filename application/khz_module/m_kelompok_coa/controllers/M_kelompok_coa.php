	$this->form_validation->set_rules('nama_kelompok_coa', 'nama kelompok coa', 'trim|required');

	$this->form_validation->set_rules('uniqid', 'uniqid', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

