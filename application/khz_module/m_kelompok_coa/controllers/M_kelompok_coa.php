    public function read($id) 
    {
        $row = $this->Model_kelompok_coa->get_by_id($id);
        if ($row) {
            $data = array(
		'uniqid' => $row->uniqid,
		'id_kelompok_coa' => $row->id_kelompok_coa,
		'nama_kelompok_coa' => $row->nama_kelompok_coa,
	    );
           $this->template->load('template_admin','m_kelompok_coa/m_kelompok_coa_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(base_url('m_kelompok_coa'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Tambah',
            'action' => base_url('m_kelompok_coa/create_action'),
	    'uniqid' => set_value('uniqid'),
	    'id_kelompok_coa' => set_value('id_kelompok_coa'),
	    'nama_kelompok_coa' => set_value('nama_kelompok_coa'),
	);
       $this->template->load('template_admin','m_kelompok_coa/m_kelompok_coa_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'id_kelompok_coa' => $this->input->post('id_kelompok_coa',TRUE),
		'nama_kelompok_coa' => $this->input->post('nama_kelompok_coa',TRUE),
	    );

            $this->Model_kelompok_coa->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(base_url('m_kelompok_coa'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Model_kelompok_coa->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Ubah',
                'action' => base_url('m_kelompok_coa/update_action'),
		'uniqid' => set_value('uniqid', $row->uniqid),
		'id_kelompok_coa' => set_value('id_kelompok_coa', $row->id_kelompok_coa),
		'nama_kelompok_coa' => set_value('nama_kelompok_coa', $row->nama_kelompok_coa),
	    );
           $this->template->load('template_admin','m_kelompok_coa/m_kelompok_coa_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(base_url('m_kelompok_coa'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('uniqid', TRUE));
        } else {
            $data = array(
		'id_kelompok_coa' => $this->input->post('id_kelompok_coa',TRUE),
		'nama_kelompok_coa' => $this->input->post('nama_kelompok_coa',TRUE),
	    );

            $this->Model_kelompok_coa->update($this->input->post('uniqid', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(base_url('m_kelompok_coa'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Model_kelompok_coa->get_by_id($id);

        if ($row) {
            $this->Model_kelompok_coa->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(base_url('m_kelompok_coa'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(base_url('m_kelompok_coa'));
        }
    }

